    #[table_name = "test_minimal"]
    struct TestMinimal {
        #[primary_key]
        id: i32,
        name: String,
    }
    
    // LifeModel generates:
    // - Entity (with LifeModelTrait implementation)
    // - TestMinimalModel (the Model struct)
    // - Column enum
    // - PrimaryKey enum
    
    // Note: FromRow is not automatically generated by LifeModel
    // It must be applied separately. For this minimal test, we just verify
    // that the types exist without using FromRow-dependent methods.
    
    // Step 1: Verify Entity exists
    let _entity = Entity;
    
    // Step 2: Verify Model exists (generated by LifeModel)
    // We can't construct it without FromRow, so we just verify the type exists
    let _model_exists = std::any::type_name::<TestMinimalModel>();
    
    // Step 3: Verify Column enum exists
    let _column = Column::Id;
    
    // Step 4: Verify PrimaryKey enum exists
